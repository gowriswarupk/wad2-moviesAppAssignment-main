import fetch from 'node-fetch';

export const getMovies = () => {
    return fetch(
      '/api/movies/'
    )
      .then((response) => {
        if (!response.ok) {
          throw new Error(response.json().message);
        }
        return response.json();
      })
      .catch((error) => {
        throw error;
      });
  };
  
